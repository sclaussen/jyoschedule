function weekToDate(week) {
    if (week === 1) {
        return '12-03-23';
    }
    if (week === 2) {
        return '12-10-23';
    }
    if (week === 3) {
        return '12-17-23';
    }
    if (week === 4) {
        return '01-07-23';
    }
    if (week === 5) {
        return '01-14-23';
    }
    if (week === 6) {
        return '01-21-23';
    }
    if (week === 7) {
        return '01-28-23';
    }
    if (week === 8) {
        return '02-04-23';
    }
    if (week === 9) {
        return '02-18-23';
    }
    if (week === 10) {
        return '02-25-23';
    }
}

module.exports = weekToDate;
